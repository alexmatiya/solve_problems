# При обработке текстовых данных с извлечением записей мы разбиваем текст на отдельные строки.
# Предположим, каждая строка либо содержит одну дей-ствительную запись, либо не содержит ни одной.
# Сможете ли вы найти шаблон, который обрабатывает весь текст без разбиения данных на множество строк?
# ПОДСКАЗКА Каждая строка завершается символом новой строки (\n).
# Включите этот символ в свой шаблон.

import re

# Предположим, что текст содержит записи, разделенные символом новой строки
text = """
Record 1: John Doe, 30, New York
Record 2: Jane Smith, 25, Los Angeles
Record 3: Michael Johnson, 35, Chicago
"""

# Шаблон для поиска записей
pattern = r"Record \d+:\s*(\w+\s\w+),\s*(\d+),\s*(\w+\s\w+)\n"

# Используем re.findall для извлечения всех записей из текста
records = re.findall(pattern, text)

# Выводим извлеченные записи
for record in records:
    name, age, city = record
    print(f"Name: {name}, Age: {age}, City: {city}")
