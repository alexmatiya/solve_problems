# В этой главе вы научились сортировать задачи по степеням срочности, как по-казано в листинге 3.4. 
# Сможете ли вы построить решение для упорядочения задач по длине описания? Чем длиннее описание, тем выше ранг задачи.
# ПОДСКАЗКА Нестандартная сортировка требует использования параметра key функции sort. 
# Встроенная функция len может проверить длину строки.

tasks = [
    (1, "Задача 1: Короткое описание"),
    (2, "Задача 2: Более длинное описание с подробностями"),
    (3, "Задача 3: Очень длинное описание, которое занимает несколько строк и содержит много деталей")
]

# Сортируем задачи по длине описания
tasks.sort(key=lambda x: len(x[1]), reverse=True)

print("Задачи, отсортированные по длине описания:")
for task in tasks:
    print(f"ID: {task[0]}, Описание: {task[1]}")
