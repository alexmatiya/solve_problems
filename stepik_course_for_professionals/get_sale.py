# –°–∫–∏–¥–∫–∞ 50% üö©üö©üö©: —Ä–µ—à–∏—Ç–µ –∑–∞–¥–∞—á—É –°–∞–Ω—Ç—ã –¥–ª—è n = 18, —Ç–æ –µ—Å—Ç—å –ø–æ—Å—á–∏—Ç–∞–π—Ç–µ —Å—É–º–º—É —Ü–∏—Ñ—Ä –≤–æ –≤—Å–µ—Ö —á–∏—Å–ª–∞—Ö –æ—Ç 1 –¥–æ 1 000 000 000 000 000 000.


def get_sale_fast(number: int) -> int:
    """–í—ã—á–∏—Å–ª—è–µ—Ç —Å—É–º–º—É —Ü–∏—Ñ—Ä –≤ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –æ—Ç 1 –¥–æ 10 –≤ —Å—Ç–µ–ø–µ–Ω–∏ number"""
    k = int(str(number) + str((number - 1) * "0"))
    result = sum(i * k for i in range(1, 10)) + 1
    print(f"–°—É–º–º–∞ —Ü–∏—Ñ—Ä –æ—Ç 1 –¥–æ 10 –≤ —Å—Ç–µ–ø–µ–Ω–∏ {number} = {result}")


def get_sale(n: int) -> int:
    result = sum([int(j) for i in range(1, 10**n) for j in str(i)]) + 1
    print(f"–°—É–º–º–∞ —Ü–∏—Ñ—Ä –æ—Ç 1 –¥–æ 10 –≤ —Å—Ç–µ–ø–µ–Ω–∏ {n} = {result}")


